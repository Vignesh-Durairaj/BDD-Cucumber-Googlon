package com.credijusto.googlon.pojo;

public class GooglonText {

	private String googlonString;
	
	private long prepositionCount;
	
	private long verbCount;
	
	private long subjunctivesCount;
	
	private String orderedVocabularySet;
	
	private long prettyNosCount;

	public String getGooglonString() {
		return googlonString;
	}

	public void setGooglonString(String googlonString) {
		this.googlonString = googlonString;
	}

	public long getPrepositionCount() {
		return prepositionCount;
	}

	public void setPrepositionCount(long prepositionCount) {
		this.prepositionCount = prepositionCount;
	}

	public long getVerbCount() {
		return verbCount;
	}

	public void setVerbCount(long verbCount) {
		this.verbCount = verbCount;
	}

	public long getSubjunctivesCount() {
		return subjunctivesCount;
	}

	public void setSubjunctivesCount(long subjunctivesCount) {
		this.subjunctivesCount = subjunctivesCount;
	}

	public String getOrderedVocabularySet() {
		return orderedVocabularySet;
	}

	public void setOrderedVocabularySet(String orderedVocabularySet) {
		this.orderedVocabularySet = orderedVocabularySet;
	}

	public long getPrettyNosCount() {
		return prettyNosCount;
	}

	public void setPrettyNosCount(long prettyNosCount) {
		this.prettyNosCount = prettyNosCount;
	}

}
